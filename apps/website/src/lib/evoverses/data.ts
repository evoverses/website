export const getNatureEffect = (nature: Nature, stat: StatName) => {
  const effects = natureEffects[nature];
  if (effects.positive === stat) {
    return 1;
  } else if (effects.negative === stat) {
    return -1;
  } else {
    return 0;
  }
};
